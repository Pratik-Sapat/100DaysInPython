p_keys = ["a", "b", "c", "d", "e", "f", "g"]
print(p_keys[2:5])
print(p_keys[2:]) #till end of list.
print(p_keys[:5]) #up to position 5 all item.
print(p_keys[2:5:2]) #position 2 to position 5 with 2 steps.
print(p_keys[::2]) #every second item in list.
print(p_keys[::-1]) #reverse the list.


